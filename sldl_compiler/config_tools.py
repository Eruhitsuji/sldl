def load_config_json(path: str | Path) -> tuple[dict[str, Any] | None, list[Diagnostic]]:
    path=Path(path)
    try:
        source=path.read_text(encoding="utf-8")
    except OSError as exc:
        return None, [Diagnostic("error", "E_CONFIG_FILE_READ", f"Cannot read config file: {path} ({exc})")]
    try:
        data=json.loads(source)
    except json.JSONDecodeError as exc:
        return None, [Diagnostic("error", "E_CONFIG_JSON", f"Invalid JSON in config file: {path}: {exc.msg}", exc.lineno, exc.colno)]
    if(not isinstance(data, dict)):
        return None, [Diagnostic("error", "E_CONFIG_ROOT", "Config root must be a JSON object")]
    return data, []


def detect_config_type(data: dict[str, Any]) -> tuple[str | None, Diagnostic | None]:
    value=data.get("config_type")
    if(isinstance(value, str) and value.strip()):
        return value.strip(), None
    inferred=None
    if("documents" in data and "project" in data):
        inferred="sldl.build_manifest"
    elif("documents" in data):
        inferred="sldl.project"
    elif("document_types" in data or "logic_rules" in data or "node_rules" in data or "object_classes" in data):
        inferred="sldl.schema"
    elif("engine" in data or "dvi_to_pdf" in data or "steps" in data or "cleanup" in data):
        inferred="sldl.latex_build"
    elif("checks" in data and "summary" in data):
        inferred="sldl.release_manifest"
    elif("files" in data and any(isinstance(item, dict) and "sha256" in item for item in data.get("files", []) if isinstance(data.get("files"), list))):
        inferred="sldl.snapshot_manifest"
    elif("project_files" in data or "build_project_files" in data or "golden_snapshot" in data):
        inferred="sldl.release_check"
    elif("templates" in data and data.get("config_type")=="sldl.template_reference"):
        inferred="sldl.template_reference"
    elif("templates" in data):
        inferred="sldl.template_manifest"
    elif("labels" in data or "html_lang" in data):
        inferred="sldl.export_labels"
    if(inferred):
        return inferred, Diagnostic("warning", "W_CONFIG_TYPE_MISSING", f"config_type is missing; inferred {inferred}")
    return None, Diagnostic("error", "E_CONFIG_TYPE_MISSING", "config_type is missing and could not be inferred")


def check_config_file(path: str | Path, expected_type: str | None = None, check_paths: bool = True) -> list[Diagnostic]:
    path=Path(path)
    data,diagnostics=load_config_json(path)
    if(data is None):
        return diagnostics
    config_type,inferred_diag=detect_config_type(data)
    if(inferred_diag):
        diagnostics.append(inferred_diag)
    if(not config_type):
        return diagnostics
    if(config_type not in SUPPORTED_CONFIG_TYPES):
        diagnostics.append(Diagnostic("error", "E_CONFIG_TYPE_UNKNOWN", f"Unknown config_type: {config_type}"))
        return diagnostics
    if(expected_type and config_type!=expected_type):
        diagnostics.append(Diagnostic("error", "E_CONFIG_TYPE_MISMATCH", f"Expected {expected_type}, got {config_type}"))
    diagnostics.extend(_check_common_metadata(data, config_type))
    base_dir=path.parent
    if(config_type=="sldl.schema"):
        diagnostics.extend(check_schema_files([path]))
    elif(config_type=="sldl.project"):
        diagnostics.extend(_check_project_config(data, base_dir, check_paths))
    elif(config_type=="sldl.export_labels"):
        diagnostics.extend(_check_export_labels_config(data))
    elif(config_type=="sldl.latex_build"):
        diagnostics.extend(_check_latex_build_config(data))
    elif(config_type=="sldl.template_manifest"):
        diagnostics.extend(_check_template_manifest(data, base_dir, check_paths, path))
    elif(config_type=="sldl.template_reference"):
        diagnostics.extend(_check_template_reference(data))
    elif(config_type=="sldl.build_manifest"):
        diagnostics.extend(_check_build_manifest(data))
    elif(config_type=="sldl.release_check"):
        diagnostics.extend(_check_release_check_config(data, base_dir, check_paths))
    elif(config_type=="sldl.release_manifest"):
        diagnostics.extend(_check_release_manifest(data))
    elif(config_type=="sldl.snapshot_manifest"):
        diagnostics.extend(_check_snapshot_manifest(data, base_dir, check_paths))
    return diagnostics


def config_summary(path: str | Path) -> dict[str, Any]:
    data,diagnostics=load_config_json(path)
    if(data is None):
        return {"path": str(path), "config_type": None, "diagnostics": [d.to_dict() for d in diagnostics]}
    config_type,inferred_diag=detect_config_type(data)
    if(inferred_diag):
        diagnostics.append(inferred_diag)
    summary={
        "path": str(path),
        "config_type": config_type,
        "description": data.get("description", ""),
        "version": data.get("version", data.get("schema_version", "")),
        "top_level_keys": sorted(data.keys()),
        "diagnostics": [d.to_dict() for d in diagnostics],
    }
    if(config_type=="sldl.project"):
        docs=data.get("documents") if(isinstance(data.get("documents"), list)) else []
        summary["documents"]=len(docs)
        summary["outputs"]=sum(len(doc.get("outputs", [])) for doc in docs if(isinstance(doc, dict)))
    elif(config_type=="sldl.schema"):
        doc_types=data.get("document_types") if(isinstance(data.get("document_types"), dict)) else {}
        summary["document_types"]=sorted(doc_types.keys())
    elif(config_type=="sldl.export_labels"):
        labels=data.get("labels") if(isinstance(data.get("labels"), dict)) else {k:v for k,v in data.items() if(k not in {"config_type", "description", "html_lang", "version", "schema_version", "$schema"})}
        summary["labels"]=sorted(labels.keys())
    elif(config_type=="sldl.latex_build"):
        summary["dry_run"]=data.get("dry_run")
        summary["uses_steps"]=isinstance(data.get("steps"), list)
    elif(config_type=="sldl.template_manifest"):
        templates=data.get("templates") if(isinstance(data.get("templates"), list)) else []
        summary["templates"]=len(templates)
    elif(config_type=="sldl.template_reference"):
        templates=data.get("templates") if(isinstance(data.get("templates"), list)) else []
        summary["templates"]=len(templates)
    elif(config_type=="sldl.build_manifest"):
        documents=data.get("documents") if(isinstance(data.get("documents"), list)) else []
        summary["documents"]=len(documents)
    elif(config_type=="sldl.release_check"):
        summary["project_files"]=len(data.get("project_files", [])) if(isinstance(data.get("project_files"), list)) else 0
        summary["config_files"]=len(data.get("config_files", [])) if(isinstance(data.get("config_files"), list)) else 0
    elif(config_type=="sldl.release_manifest"):
        checks=data.get("checks") if(isinstance(data.get("checks"), list)) else []
        summary["checks"]=len(checks)
        summary["failed"]=(data.get("summary") or {}).get("failed") if(isinstance(data.get("summary"), dict)) else None
    elif(config_type=="sldl.snapshot_manifest"):
        files=data.get("files") if(isinstance(data.get("files"), list)) else []
        summary["files"]=len(files)
    return summary


def explain_config_type(config_type: str) -> str:
    if(config_type not in SUPPORTED_CONFIG_TYPES):
        raise ValueError(f"Unknown config type: {config_type}")
    spec=SUPPORTED_CONFIG_TYPES[config_type]
    lines=[f"# {config_type}", "", spec["title"], "", spec["description"], ""]
    lines.append("## Required keys")
    lines.append("")
    for key in spec["required"]:
        lines.append(f"- `{key}`")
    lines.append("")
    lines.append("## Important keys")
    lines.append("")
    for key in spec["important"]:
        lines.append(f"- `{key}`")
    lines.append("")
    lines.append("## Minimal example")
    lines.append("")
    lines.append("```json")
    lines.append(json.dumps(init_config_data(config_type), ensure_ascii=False, indent=2))
    lines.append("```")
    return "\n".join(lines)+"\n"

def explain_config_file(path: str | Path) -> str:
    summary=config_summary(path)
    config_type=summary.get("config_type")
    lines=[f"# Config file: {path}", "", f"- config_type: `{config_type or '-'}`", f"- description: {summary.get('description') or '-'}", f"- version: {summary.get('version') or '-'}", ""]
    diagnostics=check_config_file(path)
    lines.append("## Diagnostics")
    lines.append("")
    if(diagnostics):
        for diag in diagnostics:
            lines.append(f"- {diag.level.upper()} `{diag.code}`: {diag.message}")
    else:
        lines.append("- None")
    lines.append("")
    lines.append("## Top-level keys")
    lines.append("")
    for key in summary.get("top_level_keys", []):
        lines.append(f"- `{key}`")
    if(config_type in SUPPORTED_CONFIG_TYPES):
        lines.append("")
        lines.append("## Type reference")
        lines.append("")
        lines.append(SUPPORTED_CONFIG_TYPES[config_type]["description"])
    return "\n".join(lines)+"\n"


