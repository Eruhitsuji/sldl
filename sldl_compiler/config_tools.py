def _check_latex_build_config(data: dict[str, Any]) -> list[Diagnostic]:
    diagnostics=[]
    for key in ("dry_run", "stop_on_error", "show_output"):
        if(key in data and not isinstance(data.get(key), bool)):
            diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_BOOL", f"{key} must be a boolean"))
    steps=data.get("steps")
    if(steps is not None):
        if(not isinstance(steps, list)):
            diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_STEPS", "steps must be a list"))
        else:
            for i,step in enumerate(steps):
                _check_latex_step(step, diagnostics, f"steps[{i}]")
    engine=data.get("engine")
    if(engine is not None):
        if(not isinstance(engine, dict)):
            diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_ENGINE", "engine must be an object"))
        else:
            if(engine.get("enabled", True)):
                _check_command_object(engine, diagnostics, "engine")
            if("passes" in engine and not isinstance(engine.get("passes"), int)):
                diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_PASSES", "engine.passes must be an integer"))
    dvi=data.get("dvi_to_pdf")
    if(dvi is not None):
        if(not isinstance(dvi, dict)):
            diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_DVI", "dvi_to_pdf must be an object"))
        elif(dvi.get("enabled", True)):
            _check_command_object(dvi, diagnostics, "dvi_to_pdf")
    cleanup=data.get("cleanup")
    if(cleanup is not None):
        if(not isinstance(cleanup, dict)):
            diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_CLEANUP", "cleanup must be an object"))
        else:
            if("enabled" in cleanup and not isinstance(cleanup.get("enabled"), bool)):
                diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_CLEANUP_BOOL", "cleanup.enabled must be a boolean"))
            for key in ("extensions", "files"):
                if(key in cleanup and not _is_string_list(cleanup.get(key))):
                    diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_CLEANUP_LIST", f"cleanup.{key} must be a list of strings"))
    return diagnostics


def _check_latex_step(step: Any, diagnostics: list[Diagnostic], prefix: str) -> None:
    if(not isinstance(step, dict)):
        diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_STEP", f"{prefix} must be an object"))
        return
    _check_command_object(step, diagnostics, prefix)
    if("repeat" in step and not isinstance(step.get("repeat"), int)):
        diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_REPEAT", f"{prefix}.repeat must be an integer"))


def _check_command_object(obj: dict[str, Any], diagnostics: list[Diagnostic], prefix: str) -> None:
    if(not isinstance(obj.get("command"), str) or not str(obj.get("command")).strip()):
        diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_COMMAND", f"{prefix}.command must be a non-empty string"))
    if("args" in obj and not (isinstance(obj.get("args"), list) or isinstance(obj.get("args"), str))):
        diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_ARGS", f"{prefix}.args must be a list or string"))
    if(isinstance(obj.get("args"), list) and not all(isinstance(x, (str, int, float, bool)) for x in obj.get("args"))):
        diagnostics.append(Diagnostic("error", "E_LATEX_BUILD_ARGS", f"{prefix}.args list items must be scalar values"))


def _check_template_manifest(data: dict[str, Any], base_dir: Path, check_paths: bool, manifest_path: Path | None = None) -> list[Diagnostic]:
    diagnostics=[]
    templates=data.get("templates")
    if(isinstance(templates, dict)):
        template_items=[]
        for name,value in templates.items():
            if(isinstance(value, dict)):
                item=dict(value)
                item.setdefault("name", str(name))
                template_items.append(item)
            else:
                diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_TEMPLATE", f"templates.{name} must be an object"))
        templates=template_items
    if(not isinstance(templates, list) or not templates):
        diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_TEMPLATES", "templates must be a non-empty list or object"))
        return diagnostics
    manifest_role=data.get("manifest_role")
    if(manifest_role is not None and manifest_role not in {"canonical", "legacy_compatibility", "adhoc"}):
        diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_ROLE", "manifest_role must be canonical, legacy_compatibility, or adhoc"))
    if(manifest_path is not None and manifest_path.name=="manifest.json"):
        diagnostics.append(Diagnostic("warning", "W_TEMPLATE_MANIFEST_LEGACY", "templates/manifest.json is a legacy compatibility copy; templates/template_manifest.json is canonical in v1.0.5"))
        canonical_value=data.get("canonical_manifest")
        if(canonical_value is not None and canonical_value!="template_manifest.json"):
            diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_CANONICAL", "legacy manifest canonical_manifest must be template_manifest.json"))
    if(manifest_path is not None and manifest_path.name=="template_manifest.json" and manifest_role=="legacy_compatibility"):
        diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_ROLE", "template_manifest.json must not be marked as legacy_compatibility"))
    allowed_keys={
        "name", "description", "document_type", "language", "path", "template_file",
        "schema", "default_export_config", "default_latex_build_config", "strict_schema"
    }
    seen=set()
    for i,item in enumerate(templates):
        loc=f"templates[{i}]"
        if(not isinstance(item, dict)):
            diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_TEMPLATE", f"{loc} must be an object"))
            continue
        for key in item.keys():
            if(key not in allowed_keys):
                diagnostics.append(Diagnostic("warning", "W_TEMPLATE_MANIFEST_UNKNOWN_KEY", f"{loc}.{key} is not a known template manifest field"))
        for key in ("name", "description", "document_type", "language"):
            if(not isinstance(item.get(key), str) or not item.get(key)):
                diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_FIELD", f"{loc}.{key} must be a non-empty string"))
        path_value=item.get("path", item.get("template_file"))
        if(not isinstance(path_value, str) or not path_value):
            diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_FIELD", f"{loc}.path must be a non-empty string"))
        elif(check_paths):
            _warn_missing_path(base_dir/path_value, diagnostics, f"{loc}.path")
        name=item.get("name")
        if(isinstance(name, str)):
            if(name in seen):
                diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_DUPLICATE", f"duplicate template name: {name}"))
            seen.add(name)
        for key in ("schema", "default_export_config", "default_latex_build_config"):
            value=item.get(key)
            if(value is None):
                continue
            if(not isinstance(value, str) or not value):
                diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_FIELD", f"{loc}.{key} must be a non-empty string"))
            elif(check_paths):
                _warn_missing_path(base_dir/value, diagnostics, f"{loc}.{key}")
        if("strict_schema" in item and not isinstance(item.get("strict_schema"), bool)):
            diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_STRICT_SCHEMA", f"{loc}.strict_schema must be a boolean"))
        if(check_paths):
            _check_template_manifest_bound_file_types(item, base_dir, diagnostics, loc)
    if(check_paths):
        listed_paths=set()
        for item in templates:
            if(isinstance(item, dict)):
                path_value=item.get("path", item.get("template_file"))
                if(isinstance(path_value, str) and path_value):
                    listed_paths.add((base_dir/path_value).resolve())
        for sldl_path in sorted(base_dir.glob("*.sldl")):
            if(sldl_path.resolve() not in listed_paths):
                diagnostics.append(Diagnostic("warning", "W_TEMPLATE_MANIFEST_UNLISTED_TEMPLATE", f"template file is not listed in manifest: {sldl_path.name}"))
        if(manifest_path is not None):
            sibling_name="manifest.json" if(manifest_path.name=="template_manifest.json") else "template_manifest.json"
            sibling=manifest_path.parent/sibling_name
            if(sibling.exists()):
                sibling_data,sibling_diags=load_config_json(sibling)
                for diag in sibling_diags:
                    diagnostics.append(Diagnostic(diag.level, diag.code, f"{sibling.name}: {diag.message}", diag.line, diag.column))
                if(isinstance(sibling_data, dict) and _template_manifest_signature(data)!=_template_manifest_signature(sibling_data)):
                    diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_COMPATIBILITY", f"{manifest_path.name} and {sibling.name} declare different template sets"))
    return diagnostics


def _template_manifest_signature(data: dict[str, Any]) -> list[tuple[str, str, str, str]]:
    templates=data.get("templates")
    if(isinstance(templates, dict)):
        items=[]
        for name,value in templates.items():
            if(isinstance(value, dict)):
                item=dict(value)
                item.setdefault("name", str(name))
                items.append(item)
    elif(isinstance(templates, list)):
        items=[item for item in templates if(isinstance(item, dict))]
    else:
        items=[]
    signature=[]
    for item in items:
        signature.append((
            str(item.get("name", "")),
            str(item.get("path", item.get("template_file", ""))),
            str(item.get("document_type", "")),
            str(item.get("schema", "")),
        ))
    return sorted(signature)


def _check_template_manifest_bound_file_types(item: dict[str, Any], base_dir: Path, diagnostics: list[Diagnostic], loc: str) -> None:
    expected={
        "schema": "sldl.schema",
        "default_export_config": "sldl.export_labels",
        "default_latex_build_config": "sldl.latex_build",
    }
    loaded: dict[str, dict[str, Any]]={}
    for key,expected_type in expected.items():
        value=item.get(key)
        if(not isinstance(value, str) or not value):
            continue
        path=base_dir/value
        if(not path.exists()):
            continue
        data,sub_diags=load_config_json(path)
        if(data is None):
            for diag in sub_diags:
                diagnostics.append(Diagnostic(diag.level, diag.code, f"{loc}.{key}: {diag.message}", diag.line, diag.column))
            continue
        config_type,inferred_diag=detect_config_type(data)
        if(inferred_diag and inferred_diag.level=="error"):
            diagnostics.append(Diagnostic("error", inferred_diag.code, f"{loc}.{key}: {inferred_diag.message}"))
        if(config_type!=expected_type):
            diagnostics.append(Diagnostic("error", "E_TEMPLATE_MANIFEST_BOUND_CONFIG_TYPE", f"{loc}.{key} must reference {expected_type}, got {config_type or '-'}"))
        loaded[key]=data
    schema_data=loaded.get("schema")
    document_type=item.get("document_type")
    if(schema_data is not None and isinstance(document_type, str) and document_type):
        document_types=schema_data.get("document_types")
        if(not isinstance(document_types, dict) or document_type not in document_types):
            diagnostics.append(Diagnostic("error", "E_TEMPLATE_SCHEMA_DOCUMENT_TYPE", f"{loc}.document_type {document_type} is not defined by the bound schema"))


def _check_template_reference(data: dict[str, Any]) -> list[Diagnostic]:
    diagnostics=[]
    templates=data.get("templates")
    if(not isinstance(templates, list) or not templates):
        diagnostics.append(Diagnostic("error", "E_TEMPLATE_REFERENCE_TEMPLATES", "templates must be a non-empty list"))
        return diagnostics
    seen=set()
    for i,item in enumerate(templates):
        loc=f"templates[{i}]"
        if(not isinstance(item, dict)):
            diagnostics.append(Diagnostic("error", "E_TEMPLATE_REFERENCE_TEMPLATE", f"{loc} must be an object"))
            continue
        name=item.get("name")
        if(not isinstance(name, str) or not name):
            diagnostics.append(Diagnostic("error", "E_TEMPLATE_REFERENCE_NAME", f"{loc}.name must be a non-empty string"))
        elif(name in seen):
            diagnostics.append(Diagnostic("error", "E_TEMPLATE_REFERENCE_DUPLICATE", f"duplicate template name: {name}"))
        else:
            seen.add(name)
        for key in ("document_type", "language", "schema", "manifest_role"):
            if(key in item and item.get(key) is not None and not isinstance(item.get(key), str)):
                diagnostics.append(Diagnostic("error", "E_TEMPLATE_REFERENCE_FIELD", f"{loc}.{key} must be a string or null"))
    return diagnostics


def _check_build_manifest(data: dict[str, Any]) -> list[Diagnostic]:
    diagnostics=[]
    documents=data.get("documents")
    if(not isinstance(documents, list)):
        diagnostics.append(Diagnostic("error", "E_BUILD_MANIFEST_DOCUMENTS", "documents must be a list"))
        return diagnostics
    for i,doc in enumerate(documents):
        if(not isinstance(doc, dict)):
            diagnostics.append(Diagnostic("error", "E_BUILD_MANIFEST_DOCUMENT", f"documents[{i}] must be an object"))
            continue
        if("outputs" in doc and not isinstance(doc.get("outputs"), list)):
            diagnostics.append(Diagnostic("error", "E_BUILD_MANIFEST_OUTPUTS", f"documents[{i}].outputs must be a list"))
        if("template" in doc and doc.get("template") is not None):
            template=doc.get("template")
            if(not isinstance(template, dict)):
                diagnostics.append(Diagnostic("error", "E_BUILD_MANIFEST_TEMPLATE", f"documents[{i}].template must be an object or null"))
            else:
                for key in ("name", "source", "manifest", "manifest_role", "declared_document_type", "schema", "export_config", "latex_build_config", "source_sha256", "manifest_sha256", "schema_sha256", "export_config_sha256", "latex_build_config_sha256"):
                    if(key in template and template.get(key) is not None and not isinstance(template.get(key), str)):
                        diagnostics.append(Diagnostic("error", "E_BUILD_MANIFEST_TEMPLATE_FIELD", f"documents[{i}].template.{key} must be a string or null"))
    return diagnostics


def _check_release_check_config(data: dict[str, Any], base_dir: Path, check_paths: bool) -> list[Diagnostic]:
    diagnostics=[]
    if("base_dir" in data and not isinstance(data.get("base_dir"), str)):
        diagnostics.append(Diagnostic("error", "E_RELEASE_BASE_DIR", "base_dir must be a string"))
    actual_base=base_dir
    if(isinstance(data.get("base_dir"), str)):
        value=Path(data["base_dir"])
        actual_base=value if(value.is_absolute()) else base_dir/value
    for key in ("required_files", "forbidden_paths", "forbidden_globs", "config_files", "project_files", "build_project_files", "build_manifest_files", "compile_paths"):
        value=data.get(key)
        if(value is None):
            continue
        if(not _is_string_list(value)):
            diagnostics.append(Diagnostic("error", "E_RELEASE_LIST", f"{key} must be a list of strings"))
            continue
        if(check_paths and key not in {"build_manifest_files", "forbidden_paths", "forbidden_globs"}):
            for i,item in enumerate(value):
                _warn_missing_path(actual_base/item, diagnostics, f"{key}[{i}]")
    if("compileall" in data and not isinstance(data.get("compileall"), bool)):
        diagnostics.append(Diagnostic("error", "E_RELEASE_COMPILEALL", "compileall must be a boolean"))
    commands=data.get("commands")
    if(commands is not None):
        if(not isinstance(commands, list)):
            diagnostics.append(Diagnostic("error", "E_RELEASE_COMMANDS", "commands must be a list"))
        else:
            for i,command in enumerate(commands):
                if(not isinstance(command, dict)):
                    diagnostics.append(Diagnostic("error", "E_RELEASE_COMMAND", f"commands[{i}] must be an object"))
                    continue
                if("args" not in command or not _is_string_list(command.get("args"))):
                    diagnostics.append(Diagnostic("error", "E_RELEASE_COMMAND_ARGS", f"commands[{i}].args must be a list of strings"))
                if("expect_failure" in command and not isinstance(command.get("expect_failure"), bool)):
                    diagnostics.append(Diagnostic("error", "E_RELEASE_COMMAND_EXPECT_FAILURE", f"commands[{i}].expect_failure must be a boolean"))
    snapshot=data.get("golden_snapshot")
    if(snapshot is not None):
        if(not isinstance(snapshot, str)):
            diagnostics.append(Diagnostic("error", "E_RELEASE_SNAPSHOT", "golden_snapshot must be a string"))
        elif(check_paths):
            _warn_missing_path(actual_base/snapshot, diagnostics, "golden_snapshot")
    return diagnostics


