def _check_latex_block(value: Any, diagnostics: list[Diagnostic], prefix: str) -> None:
    if(value is None):
        return
    if(not isinstance(value, dict)):
        diagnostics.append(Diagnostic("error", "E_LATEX_OPTIONS", f"{prefix} must be an object"))
        return
    for key,item in value.items():
        if(key not in KNOWN_LATEX_KEYS):
            diagnostics.append(Diagnostic("warning", "W_LATEX_OPTION_UNKNOWN", f"Unknown LaTeX option at {prefix}: {key}"))
        if(key in {"hyperref", "sloppy", "toc", "latex_hyperref", "latex_sloppy", "latex_toc"} and not isinstance(item, bool)):
            diagnostics.append(Diagnostic("error", "E_LATEX_OPTION_BOOL", f"{prefix}.{key} must be a boolean"))
        if(key in {"table_font", "latex_table_font", "code_font_size", "latex_code_font_size"} and item not in KNOWN_TABLE_FONT_VALUES):
            diagnostics.append(Diagnostic("warning", "W_LATEX_OPTION_VALUE", f"Unexpected value for {prefix}.{key}: {item}"))
        if(key in {"top_level", "latex_top_level"} and item not in KNOWN_TOP_LEVEL_VALUES):
            diagnostics.append(Diagnostic("warning", "W_LATEX_OPTION_VALUE", f"Unexpected value for {prefix}.{key}: {item}"))
        if(key in {"code_environment", "latex_code_environment"} and item not in KNOWN_CODE_ENV_VALUES):
            diagnostics.append(Diagnostic("warning", "W_LATEX_OPTION_VALUE", f"Unexpected value for {prefix}.{key}: {item}"))
        if(key in {"mermaid_mode", "latex_mermaid_mode"} and item not in KNOWN_MERMAID_MODE_VALUES):
            diagnostics.append(Diagnostic("warning", "W_LATEX_OPTION_VALUE", f"Unexpected value for {prefix}.{key}: {item}"))


def _check_mermaid_mode(data: dict[str, Any], diagnostics: list[Diagnostic], prefix: str) -> None:
    if("mermaid_mode" in data and data.get("mermaid_mode") not in KNOWN_MERMAID_MODE_VALUES):
        diagnostics.append(Diagnostic("warning", "W_PROJECT_MERMAID_MODE", f"Unexpected value for {prefix}.mermaid_mode: {data.get('mermaid_mode')}"))


def _warn_missing_path(path: Path, diagnostics: list[Diagnostic], label: str) -> None:
    if(not path.exists()):
        diagnostics.append(Diagnostic("warning", "W_CONFIG_PATH_MISSING", f"Referenced path for {label} does not exist: {path}"))


def _is_string_list(value: Any) -> bool:
    return isinstance(value, list) and all(isinstance(item, str) for item in value)
